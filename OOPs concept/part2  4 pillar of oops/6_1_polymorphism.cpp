
/*
Polymorphism is a core concept in object-oriented programming (OOP) that allows objects of different classes to be treated as objects of a common base class. It provides a way to perform a single action in different forms. Polymorphism enhances flexibility and reusability in code by allowing one interface to be used for a general class of actions.

*******************************************************************************

Types of Polymorphism
----------------------

1. Compile-Time Polymorphism (Static Polymorphism)

This type of polymorphism is resolved during compile time and is achieved through:

i. Function Overloading: Multiple functions with the same name but different parameter lists within the same scope.
ii. Operator Overloading: Redefining the way operators work for user-defined types.

______________________________________________________________________________________

2. Run-Time Polymorphism (Dynamic Polymorphism)

This type of polymorphism is resolved during runtime and is achieved through:

i. Virtual Functions: Functions in a base class that can be overridden by derived classes.
ii. Inheritance: Allows objects of derived classes to be treated as objects of the base class

*********************************************************************************************

*/